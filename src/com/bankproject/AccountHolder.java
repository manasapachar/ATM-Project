package com.bankproject;

public class AccountHolder {
	
	String firstName;
	String lastName;
	String address;
	int age;
	int mobileNumber;	
	
	public AccountHolder(String firstName, String lastName, String address, int age, int mobileNumber) {
		super();
		this.firstName = firstName;
		this.lastName = lastName;
		this.address = address;
		this.age = age;
		this.mobileNumber = mobileNumber;
	}

}
